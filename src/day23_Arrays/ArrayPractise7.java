package day23_Arrays;

public class ArrayPractise7 {
	
	public static void main(String[] args) {
		
		
		for(int i=0; i<=7; i++){
			for(int j=0; j<=7; j++) {
				
				if(i%2==0 && j%2==0) {
					System.out.print(" W ");
				}if(i%2==1 || j%2==1) {
					System.out.print(" B ");
				}
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
	}


}
