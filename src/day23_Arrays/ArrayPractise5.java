package day23_Arrays;

public class ArrayPractise5 {
	public static void main(String[] args) {
		int r =0;
		for(int i =0 ; i<=7; i++) {
			for(int j=1;j<=8;j++) {
				 r = j+8*i;
				System.out.print(r+"\t");
			}
			System.out.println();
			System.out.println();
			System.out.println();
		}
	}

}
