package day06_operators;

public class ShortHandOperators {
	
	public static void main(String[] args) {
		
		
		int students = 45;
		System.out.println(students);
		
		students = students+5;
		
		System.out.println(students);
		
		int teachers = 10;
		
		System.out.println(teachers);
		
		teachers+=2;
		
		System.out.println(teachers);
		
		int cars =12;
		System.out.println(cars);
		cars*=2;
		
		System.out.println(cars);
		
		
		
	}

}
