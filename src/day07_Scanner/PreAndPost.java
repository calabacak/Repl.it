package day07_Scanner;

public class PreAndPost {
	
	public static void main(String[] args) {
		
		int num = 0;
		int num2 = num++;
		System.out.println("num : "+ num);
		System.out.println("num2 : "+ num2);
		
		int n = 0;
		int n2 = ++n;
		System.out.println("n : "+ n);
		System.out.println("n2 : "+ n2);
		
		
		
		int i = 10;
		i++;
		++i;
		System.out.println(i);
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
