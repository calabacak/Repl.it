package day04_primitive_operators;

public class Primitives {
	public static void main(String[] args) {
		
		byte byteValue=127;
		short shortValue=23000;
		int intValue= 123456789;
		long longValue=123456654321L;
		
		float floatValue=12.123F;
		double doubleValue=234.45;
		
		
		char charValue='@';
		char charValue2=65;
		
		boolean booleanValue=true;
		boolean booleanValue2=false;		
		
		System.out.println("byte is "+byteValue);
		
		System.out.println("boolean value is "+booleanValue);
		System.out.println("boolean value2 is "+booleanValue2);
		System.out.println("charValue1 is "+charValue);
		System.out.println("charValue2 is "+charValue2);
		
	}

}
